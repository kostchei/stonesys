// Build Lulu print source files from handbook chapter sources.
// Usage: node handbook/tools/build-lulu-print.mjs
import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const handbookDir = join(root, "handbook");
const outDir = join(root, "print", "lulu");

const chapterFiles = readdirSync(handbookDir)
  .filter((file) => /^\d\d-.*\.md$/.test(file))
  .sort();

function readUtf8(file) {
  return readFileSync(file, "utf8").replace(/^\uFEFF/, "");
}

function cleanForPrint(markdown) {
  return markdown
    .replace(/\r\n/g, "\n")
    .replace(/^# StoneSys — The Player's Handbook/m, "# StoneSys Player's Handbook")
    .replace(/^\s*\\newpage\s*$/gm, "<!-- PAGEBREAK -->")
    .trim();
}

mkdirSync(outDir, { recursive: true });
mkdirSync(join(outDir, "cover"), { recursive: true });

const config = {
  product: "Lulu print interior",
  trim: "US Trade 6 x 9 in",
  fullBleed: false,
  page: {
    widthIn: 6,
    heightIn: 9,
  },
  marginsIn: {
    inside: 0.95,
    outside: 0.65,
    top: 0.65,
    bottom: 0.7,
  },
  typography: {
    bodyFont: "DejaVu Serif",
    bodySizePt: 9.2,
    leadingPt: 11.2,
  },
  notes: [
    "Interior and cover are separate Lulu uploads.",
    "This is a no-bleed text interior. Switch fullBleed to true only if edge-to-edge art is added.",
    "Keep cover design separate until final interior page count is known.",
  ],
};

const frontMatter = `# StoneSys Player's Handbook\n\nA player-facing reference for StoneSys campaigns.\n\n<!-- PAGEBREAK -->\n\n# Copyright and Print Notes\n\nThis interior file is generated for Lulu print production from the canonical handbook chapter sources.\n\nCover art is intentionally not included. Upload the interior PDF first, then use Lulu's cover template for the final page count.\n\n<!-- PAGEBREAK -->`;

const body = chapterFiles
  .map((file) => cleanForPrint(readUtf8(join(handbookDir, file))))
  .join("\n\n<!-- PAGEBREAK -->\n\n");

writeFileSync(join(outDir, "lulu.config.json"), `${JSON.stringify(config, null, 2)}\n`, "utf8");
writeFileSync(join(outDir, "interior.md"), `${frontMatter}\n\n${body}\n`, "utf8");
writeFileSync(
  join(outDir, "README.md"),
  `# Lulu Print Build\n\nGenerated from \`handbook/\`.\n\n## Outputs\n\n- \`interior.md\` - assembled print source\n- \`interior.pdf\` - Lulu-ready interior PDF, generated by \`build_lulu_pdf.py\`\n- \`lulu.config.json\` - trim, margin, and typography settings\n- \`cover/\` - placeholder directory for Lulu cover template/assets after final page count\n\n## Build\n\n\`\`\`powershell\nnode handbook/tools/build-lulu-print.mjs\n& 'C:\\Users\\Admin\\.cache\\codex-runtimes\\codex-primary-runtime\\dependencies\\python\\python.exe' handbook/tools/build_lulu_pdf.py\n\`\`\`\n\nThe current target is a no-bleed US Trade 6 x 9 inch text interior. Upload the cover separately in Lulu after the final page count is confirmed.\n`,
  "utf8"
);

writeFileSync(
  join(outDir, "cover", "README.md"),
  `# Cover Assets\n\nLulu cover files are generated separately from the interior. Upload \`print/lulu/interior.pdf\` first, then download Lulu's cover template for the final page count and place cover working files here.\n`,
  "utf8"
);

console.log(`Wrote Lulu print source for ${chapterFiles.length} chapters to ${outDir}`);
